package com.renuka.dto;

public class Address {
	private String stateName;
	private String districtName;
	private String constituencyName;
	private String mandalName;
	private String villageName;
	private String streetName;
	private int houseNo;
	private int pincode;
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public String getConstituencyName() {
		return constituencyName;
	}
	public void setConstituencyName(String constituencyName) {
		this.constituencyName = constituencyName;
	}
	public String getMandalName() {
		return mandalName;
	}
	public void setMandalName(String mandalName) {
		this.mandalName = mandalName;
	}
	public String getVillageName() {
		return villageName;
	}
	public void setVillageName(String villageName) {
		this.villageName = villageName;
	}
	public String getStreetName() {
		return streetName;
	}
	public void setStreetName(String streetName) {
		this.streetName = streetName;
	}
	public int getHouseNo() {
		return houseNo;
	}
	public void setHouseNo(int houseNo) {
		this.houseNo = houseNo;
	}
	public int getPincode() {
		return pincode;
	}
	public void setPincode(int pincode) {
		this.pincode = pincode;
	}
	@Override
	public String toString() {
		return "Address [stateName=" + stateName + ", districtName=" + districtName + ", constituencyName="
				+ constituencyName + ", mandalName=" + mandalName + ", villageName=" + villageName + ", streetName="
				+ streetName + ", houseNo=" + houseNo + ", pincode=" + pincode + "]";
	}
	
}