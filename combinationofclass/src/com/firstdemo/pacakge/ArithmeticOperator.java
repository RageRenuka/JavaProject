package com.firstdemo.pacakge;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int number1=10;
		int number2=20;
		int number3;
		
		number3=number1+number2;
        System.out.println(number3);
        
        //number2=20;
        number3=number1-number2;
        System.out.println(number3);
        
        number3=number1*number2;
        System.out.println(number3);
        
        number3=number1/number2;
        System.out.println(number3);
        
        
        number3=number1%number2;
        System.out.println(number3);
        
        number3=number1++;
        System.out.println(number3);
        
        number3=++number2;
        System.out.println(number3);
        
        
        number3=number1--;
        System.out.println(number3);
        
        number3=number2--;
        System.out.println(number3);
        
        
        
		

	}

}
